name = "Ashish"
age = 28
marks = 93.7

#string formatting 1st method
print("My name is %s and my age is %d and i earn %f marks"%(name,age,marks))


#string formatting 2nd method
print("My name is {} and my age is {} and i earn {} marks".format(name,age,marks))


#string formatting 3rd method
print(f"My name is {name} , my age is {age} and i earn {marks} marks")




